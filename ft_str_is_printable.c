int	ft_str_is_printable(char *str)
{
	int		a;

	a = 1;
	while (*str != '\0')
	{
		if (*str >= 32 && *str <= 126)
		{
			a = 1;
		}
		else
		{
			a = 0;
			break ;
		}
		str = str + 1;
	}
	return (a);
}
